def verificar_proximidad():
    """Verifica si el jugador est√° cerca de alg√∫n NPC."""
    global cerca_de_npc
    
    if estado_juego != "mapa":
        return
    
    dist_izq_x = abs(mi_personaje.x - npc_izquierdo.x)
    dist_izq_y = abs(mi_personaje.y - npc_izquierdo.y)
    
    dist_der_x = abs(mi_personaje.x - npc_derecho.x)
    dist_der_y = abs(mi_personaje.y - npc_derecho.y)
    
    if dist_izq_x < 25 and dist_izq_y < 25:
        cerca_de_npc = "izquierdo"
        npc_izquierdo.say("Presiona A", 500)
    elif dist_der_x < 25 and dist_der_y < 25:
        cerca_de_npc = "derecho"
        npc_derecho.say("Presiona A", 500)
    else:
        cerca_de_npc = ""

